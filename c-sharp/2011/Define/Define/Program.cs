{
    class Program
    {
        static void Main(string[] args)
        {
            if (args.Length != 1) return;
            string search=args[0];
            Console.WriteLine("Buscando:" + search);
            string code = StripHTML(get_response("http://es.wikipedia.org/wiki/" + search));
            Console.Write( code);

            /*http://es.wikipedia.org/wiki/
